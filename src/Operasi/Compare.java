package Operasi;

public class Compare {
    public static void main(String[] args) {
        // Operator Perbandingan
        /*
        * > Lebih dari
        * < Kurang dari
        * >= Lebih dari sama dengan
        * <= Kurang dari sama dengan
        * == Sama dengan
        * != Tidak sama dengan
        */

        int value1 = 1000;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);
    }
}
